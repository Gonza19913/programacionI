# CONVERSOR TEMPERATURA: de Celsius a Fahrenheit y Kelvin

# Definir la temperatura en Celsius
temp_celsius = 14

# Convertir a Fahrenheit
temp_fahrenheit = (temp_celsius * 9/5) + 32

# Convertir a Kelvin
temp_kelvin = temp_celsius + 273.15

# Mostrar los resultados
print("La Temperatura en Celsius: " + str(temp_celsius) + " C")
print("La Temperatura en Fahrenheit: " + str(temp_fahrenheit) + " F")
print("La Temperatura en Kelvin: " + str(temp_kelvin) + " K")
