# Lista original con algunos elementos duplicados
numeros = [1, 2, 3, 2, 4, 5, 1, 6, 3, 7, 8, 5]

# Crear una lista sin duplicados utilizando un conjunto
numeros_sin_duplicados = list(set(numeros))

# Imprimir la lista original y la lista sin duplicados
print("Lista original:", numeros)
print("Lista sin duplicados:", numeros_sin_duplicados)
